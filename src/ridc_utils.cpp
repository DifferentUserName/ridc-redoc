#include <iostream>
#include <stdio.h>
#include "ridc_utils.h"

using namespace std;

////////////////////////////////////////////////
// Function Name: copy_vect
// Usage: copies elements of one vector to another
//
///////////////////////////////////////////////////////
// Assumptions: both have allocated memory
//
///////////////////////////////////////////////////////
// Inputs: u, from vector
//         v, to vector
//         n, number of elements
//
///////////////////////////////////////////////////////
// Outputs:
//
///////////////////////////////////////////////////////
// Functions Called: none
//
///////////////////////////////////////////////////////
// Called By:
//
///////////////////////////////////////////////////////
void copy_vect(double *u, double *v,int n) {
  for (int i=0; i<n; i++) {
    v[i] = u[i];
  }
} //copy vect


////////////////////////////////////////////////
// Function Name: print_vect
// Usage: prints vector to screen
//
///////////////////////////////////////////////////////
// Assumptions: both have allocated memory
//
///////////////////////////////////////////////////////
// Inputs: u, vector to be printed
//         n, number of elements
//
///////////////////////////////////////////////////////
// Outputs:
//
///////////////////////////////////////////////////////
// Functions Called: none
//
///////////////////////////////////////////////////////
// Called By:
//
///////////////////////////////////////////////////////
void print_vect(double *u, int n) {
  for (int i=0; i<n; i++) {
    printf("%g \n ", u[i]);
  }
}
